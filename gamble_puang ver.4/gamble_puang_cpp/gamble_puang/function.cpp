int eventplay() //이벤트(게임시작 등등)가 플레이될 함수
{
    textcolor(15);

    if (X == 20 && Y == 10 || X == 21 && Y == 10 || X == 22 && Y == 10) //블랙잭 플레이
    {
        chack_go = false;
        Y = 9;
        return P_blackjack;
    }

    else if (X == 11 && Y == 2 || X == 11 && Y == 3 ) //포커 플레이
    {
        chack_go = false;
        X = 12;
        return P_poker;
    }

    else if (X == 17 && Y == 2 || X == 17 && Y == 4 || X == 16 && Y == 2 || X == 16 && Y == 4) //분수 이벵
    {
		chack_go = false;
        if (Y == 2) Y = 1;
        else Y = 5;
        return P_boonsu;
    }

    else if (X == 36 && Y == 6 || X == 37 && Y == 6 || X == 38 && Y == 6) //슬롯머신 플레이
    {
        chack_go = false;
        Y = 7;
        return P_slotmachine;
    }

    else if (X == 9 && Y == 9 || X == 9 && Y == 10) //홀짝 플레이
    {
        chack_go = false;
        X = 10;
        return P_holzzak;
    }

    else if (X == 46 && Y == 10 || X == 47 && Y == 10 || X == 48 && Y == 10) //경마 플레이
    {
        chack_go = false;
        Y = 9;
        return P_horse;
    }

    else if (X == 22 && Y == 4 || X == 22 && Y == 5) //매점 이벤트
    {
        chack_go = false;
        X = 21;
        return P_mart;
    }

    else
    {
        return P_none;
    }

}

